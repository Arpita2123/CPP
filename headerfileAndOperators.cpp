#include<iostream>  //click iostream with press the ctrl button to open iostream file.
using namespace std ;

int main()
{
    int a =4 , b =5 ;
    cout << "Following are the types of operators :" ;

    // Arithmatic Operators :
    cout << "The value of a + b :" << a+b << endl ;
    cout << "The value of a - b :" << a-b << endl ;
    cout << "The value of a * b :" << a*b << endl ;
    cout << "The value of a / b :" << a/b << endl ;
    cout << "The value of a % b :" << a%b << endl ;
    cout << "The value of a++ :" << a++ << endl ;
    cout << "The value of a-- :" << a-- << endl ;
    cout << "The value of ++a :" << ++a << endl ;
    cout << "The value of --a   :" << --a << endl ;

    
    return 0;
    
}